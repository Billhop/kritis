package cryptolib

import (
	"io/ioutil"
	"path/filepath"
	"testing"

	"github.com/grafeas/kritis/pkg/kritis/testutil"
)

// Uses keys and signatures generated by commands:
// `gpg --quick-generate-key --yes pgp@cryptolib.com`
// `gpg --export --armor pgp@cryptolib.com > testdata/pubkey.gpg`
// `gpg --output testdata/signature.gpg --armor -u pgp@cryptolib.com --sign testdata/payload.txt`
func TestVerifyPgp(t *testing.T) {
	tcs := []struct {
		name           string
		signature      []byte
		publicKey      []byte
		validSignature bool
	}{
		{
			name:           "valid signature and public key",
			signature:      readFile(t, "signature.gpg"),
			publicKey:      readFile(t, "publicKey.gpg"),
			validSignature: true,
		},
		{
			name:           "invalid signature",
			signature:      []byte("invalid-sig"),
			publicKey:      readFile(t, "publicKey.gpg"),
			validSignature: false,
		},
		{
			name:           "invalid public key",
			signature:      readFile(t, "signature.gpg"),
			publicKey:      []byte("invalid-public-key"),
			validSignature: false,
		},
	}

	for _, tc := range tcs {
		t.Run(tc.name, func(t *testing.T) {
			actualPayload, err := verifyPgp(tc.signature, tc.publicKey)
			testutil.CheckError(t, !tc.validSignature, err)

			// Skip payload check if signature verification failed
			if err != nil {
				return
			}

			expectedPayload := readFile(t, "payload.txt")
			if string(actualPayload) != string(expectedPayload) {
				t.Fatalf("Incorrect payload extracted: got: %s, want: %s", string(actualPayload), string(expectedPayload))
			}
		})
	}
}

func readFile(t *testing.T, filename string) []byte {
	path := filepath.Join("testdata", filename)
	contents, err := ioutil.ReadFile(path)
	if err != nil {
		t.Fatalf("Unexpected file read error: %v", err)
	}
	return contents
}
